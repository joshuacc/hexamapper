import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, "..");

const SOURCES = [
  {
    pack: "base",
    dir: path.join(repoRoot, "assets", "World Map Hex Tiles Pack", "Assets - 72 DPI (VTT)"),
  },
  {
    pack: "extended",
    dir: path.join(
      repoRoot,
      "assets",
      "World Map Hex Tiles Pack",
      "Assets - Extended -72 DPI (VTT)",
    ),
  },
];

function slugify(value) {
  return value
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "")
    .replace(/-{2,}/g, "-");
}

function normalizeCategory(name) {
  const noPrefix = name.replace(/\.png$/i, "");
  const stripped = noPrefix.startsWith("Hex - ") ? noPrefix.slice(6) : noPrefix;
  const beforeTheme = stripped.split("(")[0]?.trim() ?? stripped;
  const parts = beforeTheme.split(" - ");
  return parts[0]?.trim() || "Misc";
}

function extractTags(name, pack, category) {
  const base = name.replace(/\.png$/i, "");
  const tags = new Set([pack, slugify(category)]);

  const themeMatch = base.match(/\(([^)]+)\)/g);
  if (themeMatch) {
    for (const match of themeMatch) {
      const theme = match.slice(1, -1).trim();
      if (theme.length > 0) {
        tags.add(slugify(theme));
      }
    }
  }

  for (const word of base.split(/[\s,.-]+/)) {
    const lower = word.toLowerCase();
    if (lower.length >= 4 && /^[a-z0-9]+$/.test(lower)) {
      tags.add(lower);
    }
  }

  return [...tags].slice(0, 20);
}

function inferLayer(name, category) {
  const lower = name.toLowerCase();
  const categoryLower = category.toLowerCase();

  if (
    lower.includes("pin") ||
    lower.includes("icon") ||
    lower.includes("banner") ||
    lower.includes("title") ||
    lower.includes("vehicle") ||
    lower.includes("fog")
  ) {
    return "marker";
  }

  if (lower.startsWith("hex - base")) {
    return "base";
  }

  if (
    lower.startsWith("hex - plains") ||
    lower.startsWith("hex - hills") ||
    lower.startsWith("hex - hill") ||
    lower.startsWith("hex - water") ||
    lower.startsWith("hex - wetlands") ||
    lower.startsWith("hex - snow") ||
    lower.startsWith("hex - sparse trees") ||
    lower.startsWith("hex - forest")
  ) {
    return "base";
  }

  if (
    lower.startsWith("hex - river") ||
    lower.startsWith("hex - dirt path") ||
    lower.startsWith("hex - coast")
  ) {
    return "overlay";
  }

  if (categoryLower.includes("mountain") || categoryLower.includes("urban") || categoryLower.includes("ruin")) {
    return "overlay";
  }

  return lower.startsWith("hex -") ? "overlay" : "marker";
}

function assetUrlFromAbsolutePath(absolutePath) {
  const relativeToAssets = path.relative(path.join(repoRoot, "assets"), absolutePath);
  return `/${encodeURI(relativeToAssets.split(path.sep).join("/"))}`;
}

async function buildManifest() {
  const items = [];

  for (const source of SOURCES) {
    const entries = await fs.readdir(source.dir, { withFileTypes: true });

    for (const entry of entries) {
      if (!entry.isFile()) {
        continue;
      }

      if (!entry.name.toLowerCase().endsWith(".png")) {
        continue;
      }

      if (entry.name.includes(":Zone.Identifier")) {
        continue;
      }

      const absolutePath = path.join(source.dir, entry.name);
      const category = normalizeCategory(entry.name);
      const id = `${source.pack}:${slugify(entry.name.replace(/\.png$/i, ""))}`;

      items.push({
        id,
        name: entry.name.replace(/\.png$/i, ""),
        path: assetUrlFromAbsolutePath(absolutePath),
        pack: source.pack,
        framePx: { w: 224, h: 194 },
        category,
        tags: extractTags(entry.name, source.pack, category),
        inferredLayer: inferLayer(entry.name, category),
      });
    }
  }

  items.sort((a, b) => a.name.localeCompare(b.name));
  return items;
}

function toGeneratedModule(items) {
  const header = `// Auto-generated by scripts/generate-asset-manifest.mjs\n// Do not edit manually.\n\nimport type { AssetManifestItem } from "../domain/types";\n\nexport const ASSET_MANIFEST_VERSION = 1 as const;\n\nexport const ASSET_MANIFEST: AssetManifestItem[] = `;
  const json = JSON.stringify(items, null, 2);
  return `${header}${json} as AssetManifestItem[];\n`;
}

async function main() {
  const items = await buildManifest();

  const outputPath = path.join(repoRoot, "src", "data", "assetManifest.generated.ts");
  await fs.mkdir(path.dirname(outputPath), { recursive: true });
  await fs.writeFile(outputPath, toGeneratedModule(items), "utf8");

  process.stdout.write(`Generated ${items.length} manifest items to ${outputPath}\n`);
}

main().catch((error) => {
  process.stderr.write(`${error.stack ?? error}\n`);
  process.exitCode = 1;
});
